package com.itb.aplikasitoko.ViewModel;

public class ViewModelRekapPegawai {
    private double idpegawai;
    private String tanggal_jual;
    private String nama_pegawai;
    private String alamat_pegawai;
    private String no_pegawai;
    private String total_jual;
    private String total_pendapatan;
    private double total_keuntungan;

    public double getIdpegawai() {
        return idpegawai;
    }

    public void setIdpegawai(double idpegawai) {
        this.idpegawai = idpegawai;
    }

    public String getTanggal_jual() {
        return tanggal_jual;
    }

    public void setTanggal_jual(String tanggal_jual) {
        this.tanggal_jual = tanggal_jual;
    }

    public String getNama_pegawai() {
        return nama_pegawai;
    }

    public void setNama_pegawai(String nama_pegawai) {
        this.nama_pegawai = nama_pegawai;
    }

    public String getAlamat_pegawai() {
        return alamat_pegawai;
    }

    public void setAlamat_pegawai(String alamat_pegawai) {
        this.alamat_pegawai = alamat_pegawai;
    }

    public String getNo_pegawai() {
        return no_pegawai;
    }

    public void setNo_pegawai(String no_pegawai) {
        this.no_pegawai = no_pegawai;
    }

    public String getTotal_jual() {
        return total_jual;
    }

    public void setTotal_jual(String total_jual) {
        this.total_jual = total_jual;
    }

    public String getTotal_pendapatan() {
        return total_pendapatan;
    }

    public void setTotal_pendapatan(String total_pendapatan) {
        this.total_pendapatan = total_pendapatan;
    }

    public double getTotal_keuntungan() {
        return total_keuntungan;
    }

    public void setTotal_keuntungan(double total_keuntungan) {
        this.total_keuntungan = total_keuntungan;
    }
}
